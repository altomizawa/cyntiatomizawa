  const scrollVelocity = useVelocity(scrollY)
  const smoothVelocity = useSpring(scrollVelocity, {
    damping: 50,
    stiffness: 400,
  })
  
  // Calculate rotation and skew based on velocity
  const skewY = useTransform(smoothVelocity, [-2000, 2000], [-5, 5])
